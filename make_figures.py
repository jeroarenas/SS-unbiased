"""
Software for the paper "When Does Known Input Covariance Help Least Squares? A Closed-Form Analysis"
by J. Arenas-Garcia, C. Moriana Varo, L. K. Hansen, and J. Larsen.

The SW produces the figures included in the paper, and allow exploration of other settings

- Figure 1: closed-form EMSE of the LS and SS estimators, the cross-EMSE,
  and the EMSE of the optimal combination, shown as solid lines.  Monte Carlo
  estimates are overlaid as markers. For the combination, the markers
  correspond to the *realized* estimator with leave-one-out (LOO) estimation
  of the mixing parameter
  
- Figure 2: theoretical optimal mixing parameter lambda (solid line), 
  together with the mean of the LOO estimate (markers) and a shaded band
  of +/- one standard deviation

Model:  y = w_o^T x + e,  x ~ N(0, Sigma),  e ~ N(0, sigma_e^2),  N > M.
"""

